#Working with the file's content
#After you have read the file into memory you can do whatever 
#you want with the data

filename='pi_digits.txt'
with open (filename) as file_object:
    lines=file_object.readlines()
    pi_string = ''
    for line in lines:
        pi_string+=line.rstrip()
        print(pi_string)
        print(len(pi_string))

#One of the simplest way to save data is to write it to a file
#When you write text to a file, the o/p will still be available
#after you close the terminal containing your program's output

filename='programming.txt'
with open (filename,'w') as file_object:
    file_object.write('I Love Programming.')
    
#Writing multiple lines
#The write() fun doesn't add any new lines to the text you write
filename='programming.txt'
with open (filename,'w') as file_object:
    file_object.write('I Love Programming.')
    file_object.write('I Love Creating New Games.')
    
#Printing on next line
filename='programming.txt'
with open (filename,'w') as file_object:
    file_object.write('I Love Programming.\n')
    file_object.write('I Love Creating New Games.\n')


#Appending a file-->No old data lost, new data is added
filename='programming.txt'
with open (filename,'a') as file_object:
    #We use the 'a' argument to open the file for appending
    #rather than writing over the existing file
    file_object.write('I also love finding meaning in large datasets.\n')
    file_object.write('I love creating apps that can run in a browser.\n')
    
